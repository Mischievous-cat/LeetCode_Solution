class Solution {
    public int[] plusOne(int[] digits) {
        /**找出最长的后缀9
         当我们对数组digits加一时，我们只需要关注digits的末尾出现了多少个9即可。我们可以考虑如下的三种情况：
         如果digits的末尾没有9，例如[1,2,3]，那么我们直接将末尾的数加一，得到[1,2,4]并返回；
         如果digits的末尾有若干个9，例如[1,2,3,9,9]，那么我们只需要找出从末尾开始的第一个不为9的元素，即3，将该元素加一，得到[1,2,4,9,9]。随后将末尾的9全部置零，得到[1,2,4,0,0]并返回
         如果digits的所有元素都是9，例如[9,9,9,9,9]，那么答案为[1,0,0,0,0,0]。我们只需要构造一个长度比digits多1的新数组，将首元素置为1，其余元素置为0即可。
         我们只需要对数组digits进行一次逆序遍历，找出第一个不为9的元素，将其加一并将后续所有元素置零即可。如果digits中所有的元素均为9，那么对应着第三种情况，我们需要返回一个新的数组
         */
        int n = digits.length;
        for (int i = n-1; i >= 0; --i) {
            if (digits[i] != 9){
                ++digits[i];
                for (int j = i+1; j < n; j++) {
                    digits[j] = 0;
                }
                return digits;
            }
        }
        // digits中所有的元素均为9
        int[] ans = new int[n+1];
        ans[0] = 1;
        return ans;
    }
}
